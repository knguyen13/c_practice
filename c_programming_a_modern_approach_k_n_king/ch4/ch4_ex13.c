/* Q: Ch4, Ex13
 *
 * Only one of the expressions ++i and i++ is exactly the same as (i += 1);
 * which is it? justify your answer.
 */

/* A:
 *
 * (i += 1) is the same as ++i because the result of these expressions will be i after
 * it has been incremented.
 */